    private int[] setrandom(int num) {
        int[] order_arr = new int[num * 3];
        for (int i = 0; i < order_arr.length; i++)
            order_arr[i] = -1;
        for (int i = 0; i < order_arr.length; i++) {
            int index = (int) (Math.random()*order_arr.length);
            if (order_arr[index] == -1) {
                order_arr[index] = i;
            }
            else {
                while (order_arr[index] != -1) {
                    index++;
                    index = index % order_arr.length;
                }
                order_arr[index] = i;
            }
        }
        return order_arr;
    }
